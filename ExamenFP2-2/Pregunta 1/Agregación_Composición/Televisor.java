package Agregación_Composición;

/**
 *
 * @author asus
 */
public class Televisor {
    String marca;
    String tamaño;
    String color;

    public Televisor(String marca, String tamaño, String color) {
        this.marca = marca;
        this.tamaño = tamaño;
        this.color = color;
    }

    public String getMarca() {
        return marca;
    }

    public String getTamaño() {
        return tamaño;
    }

    public String getColor() {
        return color;
    }

    
    
}
